#!/usr/bin/env bash
set -euo pipefail

: "${GCP_PROJECT_ID:?Set GCP_PROJECT_ID}"
: "${GCP_REGION:?Set GCP_REGION}"
: "${GAR_REPOSITORY:?Set GAR_REPOSITORY}"
: "${SERVICE_NAME:=lien-automation}"
: "${IMAGE_URI:?Set IMAGE_URI}"

: "${SBR_CDP_URL:?Set SBR_CDP_URL}"
: "${SHEETS_KEY:?Set SHEETS_KEY}"
: "${SHEET_ID:?Set SHEET_ID}"
: "${SCHEDULE_RUN_TOKEN:?Set SCHEDULE_RUN_TOKEN}"

: "${SCHEDULE_TARGET_TIMEZONE:=America/New_York}"
: "${SCHEDULE_WEEKLY_DAYS:=TU,WE}"
: "${SCHEDULE_RUN_HOUR:=9}"
: "${SCHEDULE_RUN_MINUTE:=0}"
: "${SCHEDULE_DEADLINE_HOUR:=13}"
: "${SCHEDULE_DEADLINE_MINUTE:=0}"
: "${AMOUNT_MIN_COVERAGE_PCT:=95}"
: "${SCHEDULE_AUTO_THROTTLE:=1}"
: "${SCHEDULE_MAX_RECORDS:=1000}"
: "${SCHEDULE_MAX_RECORDS_FLOOR:=25}"
: "${SCHEDULE_MAX_RECORDS_CEILING:=400}"
: "${REQUIRE_OCR_TOOLS:=1}"

ENV_VARS="SBR_CDP_URL=${SBR_CDP_URL},SHEETS_KEY=${SHEETS_KEY},SHEET_ID=${SHEET_ID},SCHEDULE_RUN_TOKEN=${SCHEDULE_RUN_TOKEN},SCHEDULE_TARGET_TIMEZONE=${SCHEDULE_TARGET_TIMEZONE},SCHEDULE_WEEKLY_DAYS=${SCHEDULE_WEEKLY_DAYS},SCHEDULE_RUN_HOUR=${SCHEDULE_RUN_HOUR},SCHEDULE_RUN_MINUTE=${SCHEDULE_RUN_MINUTE},SCHEDULE_DEADLINE_HOUR=${SCHEDULE_DEADLINE_HOUR},SCHEDULE_DEADLINE_MINUTE=${SCHEDULE_DEADLINE_MINUTE},AMOUNT_MIN_COVERAGE_PCT=${AMOUNT_MIN_COVERAGE_PCT},SCHEDULE_AUTO_THROTTLE=${SCHEDULE_AUTO_THROTTLE},SCHEDULE_MAX_RECORDS=${SCHEDULE_MAX_RECORDS},SCHEDULE_MAX_RECORDS_FLOOR=${SCHEDULE_MAX_RECORDS_FLOOR},SCHEDULE_MAX_RECORDS_CEILING=${SCHEDULE_MAX_RECORDS_CEILING},REQUIRE_OCR_TOOLS=${REQUIRE_OCR_TOOLS}"

gcloud run deploy "${SERVICE_NAME}" \
  --project="${GCP_PROJECT_ID}" \
  --region="${GCP_REGION}" \
  --image="${IMAGE_URI}" \
  --platform=managed \
  --allow-unauthenticated \
  --port=8080 \
  --set-env-vars="${ENV_VARS}"

echo "Deployed Cloud Run service: ${SERVICE_NAME}"
